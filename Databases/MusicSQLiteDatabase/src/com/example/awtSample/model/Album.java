package com.example.awtSample.model;

public class Album {
    private int id;
    private String name;
    private int atist;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAtist() {
        return atist;
    }

    public void setAtist(int atist) {
        this.atist = atist;
    }
}
